// SECTION 3 – Assignment Operators

// 1. Use += on 10 with 5
let x = 10;
x += 5;
console.log(x);   // 15

// 2. Use -= on 20 with 8
let y = 20;
y -= 8;
console.log(y);   // 12

// 3. Use *= on 7 with 3
let z = 7;
z *= 3;
console.log(z);   // 21

// 4. Use /= on 40 with 5
let m = 40;
m /= 5;
console.log(m);   // 8

// 5. Use %= on 25 with 4
let n = 25;
n %= 4;
console.log(n);   // 1


